export const DEAKINS_RULES = `
CINEMATOGRAPHY RULES (Roger Deakins approach):

SHOT CONTINUITY:
- Every shot exists in context of shots before and after it
- If previous shot is WIDE, next should be MEDIUM or CLOSE (don't jump WIDE to ECU)
- If previous shot is CLOSE, you can cut to WIDE for geography reset
- Reverse shots must match eyeline — if character looks left, reverse is from right
- 180-degree rule: camera stays on one side of the action axis
- 30-degree rule: each new angle should differ by at least 30 degrees

SHOT-TO-SHOT FLOW:
- WIDE → MEDIUM → CLOSE = natural progression (comfortable)
- CLOSE → WIDE = dramatic reveal or scene shift (jarring on purpose)
- Same size back-to-back = only for shot-reverse-shot dialogue
- Never cut between two similar compositions (feels like a jump cut)

LIGHTING CONTINUITY:
- Light source direction must be consistent within a scene
- If window is on the left in WIDE, it's still on the left in CLOSE
- Mood progresses: scene starts neutral → builds to dramatic
- Night interiors: single practical source (TV, lamp, window)

LENS LANGUAGE:
- 24-35mm: wide, environmental, shows space, slight distortion = unease
- 50mm: neutral, closest to human eye, documentary feel
- 85-100mm: portrait, compression, isolates subject, intimacy
- 135mm+: extreme compression, surveillance feel, detachment

DEAKINS SPECIFICS:
- Motivated lighting only — every light has a source in the scene
- Minimal camera movement — if it moves, it has a reason
- Deep focus when showing environment, shallow for emotion
- Silhouettes and backlight for mystery
- Frames within frames (doorways, windows, mirrors)
- Negative space tells story — where character ISN'T matters
- Color temperature shifts between interior (warm) and exterior (cool)
`

